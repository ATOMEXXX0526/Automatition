#!/bin/bash
# Скрипт установки и настройки AI Assistant

set -e

echo "=========================================="
echo "AI Assistant - Установка"
echo "=========================================="

# Проверка Python версии
echo "Проверка Python версии..."
python3 --version

# Установка зависимостей
echo ""
echo "Установка зависимостей..."
pip3 install -r requirements.txt

# Создание необходимых директорий
echo ""
echo "Создание директорий..."
mkdir -p models
mkdir -p logs

# Настройка конфигурации
if [ ! -f .env ]; then
    echo ""
    echo "Создание .env файла..."
    cp .env.example .env
    echo "⚠️  Отредактируйте .env файл с вашими настройками Naumen!"
fi

# Обучение модели
echo ""
echo "Обучение модели на тестовых данных..."
python3 scripts/train_model.py

echo ""
echo "=========================================="
echo "✅ Установка завершена!"
echo "=========================================="
echo ""
echo "Следующие шаги:"
echo "1. Отредактируйте .env файл с вашими настройками"
echo "2. Запустите сервер: ./run.sh"
echo ""
